#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int	i;

	i = 0;
	if (len > 0)
	{
		while (len > 0)
		{
			*((unsigned char *)b + i) = c;
			len--;
			i++;
		}
	}
	return (b);
}
